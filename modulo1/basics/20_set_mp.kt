fun main() {
    println("--- Set de Cédulas de Empleados ---")
    println("Inmutable - sin duplicados")
    val cedulas = setOf("1234567890", "0987654321", "1234567890", "1122334455", "0987654321", "5566778899")
    println(cedulas)
    println("Operaciones de conjuntos")
    val activos = setOf("1234567890", "0987654321", "5566778899")
    println("Activos: $activos")
    val inactivos = setOf("1122334455", "9988776655")
    val conBonos = setOf("1234567890", "1122334455", "4455667788")
    println("Union activos e inactivos: ${activos union inactivos}")
    println("Intersección activos y con bonos: ${conBonos intersect activos}")
    println("Activos sin bono: ${activos subtract conBonos}")

    println("Mutable")
    val departamentos = mutableSetOf("Contabilidad", "Sistemas", "RRHH")
    println(departamentos)
    departamentos.add("Ventas")
    println(departamentos)
    departamentos.remove("RRHH")
    println(departamentos)
    println("Sistemas existe? ${"Sistemas" in departamentos}")
    println("RRHH existe? ${"RRHH" in departamentos}")
}
